	EVT_EVENT
SET
	NH_EVENT_DB_ID = 4650,
	NH_EVENT_ID = (SELECT EVENT_ID FROM EVT_EVENT WHERE EVENT_SDESC = 'WP20984-06'),
	H_EVENT_DB_ID = 4650,
	H_EVENT_ID = (SELECT EVENT_ID FROM EVT_EVENT WHERE EVENT_SDESC = 'WP20984-06')
